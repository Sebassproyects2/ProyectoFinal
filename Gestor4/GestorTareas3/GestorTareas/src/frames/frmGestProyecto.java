    //metodo para cargar la lista de usuarios en el cmb de persona reponsable
    public void cargarListaUsuarios() {
        DefaultComboBoxModel<String> modelo = (DefaultComboBoxModel<String>) cmbResponsable.getModel();
        modelo.addElement("Seleccione un responsable:");
        Usuario listusuarios = new Usuario();
        for (String x : listusuarios.todosUsuario()) {
            modelo.addElement(x);
        }
        cmbResponsable.setModel(modelo);
    }

    public void cargarDatos() {
        DefaultTableModel model = (DefaultTableModel) jtProyecto.getModel();
        model.setNumRows(0);

        GestProyecto dProyecto = new GestProyecto();
        String nombre = this.usuario.getNombre() + " ("
                + this.usuario.getEmail() + ")";
        ArrayList<Proyecto> miListaProyecto = dProyecto.buscarProyectoResponsable(nombre);

        if (!miListaProyecto.isEmpty()) {
            String datos[] = new String[6];
            for (Proyecto proyecto : miListaProyecto) {
                datos[0] = String.valueOf(proyecto.getIdProyecto());
                datos[1] = proyecto.getNombreProyecto();
                datos[2] = proyecto.getPersonaResp();
                datos[3] = String.valueOf(proyecto.getFechaInicio());
                datos[4] = String.valueOf(proyecto.getFechaFinal());
                datos[5] = proyecto.isFinalizada() ? "Sí" : "No";
                model.addRow(datos);
            }
        }
        jtProyecto.setModel(model);
    }

    public void limpiar() {
        txtIdProyecto.setText("");
        txtNombre.setText("");
        cmbResponsable.setSelectedIndex(0);
        ftxtFechaInicio.setText("");
        ftxtFechaFinal.setText("");
        cmbFinalizado.setSelectedIndex(-1);
        txtIdProyecto.requestFocus();
    }

    public boolean finalizado() {
        if (cmbFinalizado.getSelectedIndex() == 1) {
            return true;
        } else {
            return false;
        }
    }

