    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Valida si todos los espacios estan completos, si no, envia mensaje de error
        if (txtIdProyecto.getText().isEmpty()
                || txtNombre.getText().isEmpty()
                || cmbResponsable.getSelectedIndex() == 0
                || ftxtFechaInicio.getText().isEmpty()
                || ftxtFechaFinal.getText().isEmpty()
                || cmbFinalizado.getSelectedIndex() == -1) { // Verifica que se haya seleccionado una opción

            JOptionPane.showMessageDialog(this, "Rellene todos los espacios para agregar el proyecto",
                    "Espacios vacíos", JOptionPane.WARNING_MESSAGE);
            return;

        } else {
            GestProyecto gestProyecto = new GestProyecto();
            Proyecto p = new Proyecto(Integer.parseInt(txtIdProyecto.getText()),
                    txtNombre.getText(),
                    cmbResponsable.getSelectedItem().toString(),
                    LocalDate.parse(ftxtFechaInicio.getText()),
                    LocalDate.parse(ftxtFechaFinal.getText()),
                    finalizado());
            gestProyecto.insertarProyecto(p);

            cargarDatos();
            limpiar();
        }


    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DefaultTableModel model = (DefaultTableModel) jtProyecto.getModel();
        model.setNumRows(0);

        GestProyecto dProyecto = new GestProyecto();
        ArrayList<Proyecto> miListaProyecto = new ArrayList<>();

        //Creo un OptionDialog para que el usuario seleccione el tipo de búsqueda
        String[] opciones = {"Id", "Nombre", "Persona Responsable", "Fecha de Inicio",
            "Fecha de Finalización", "Estado de Finalización"};
        this.toBack();
        int x = JOptionPane.showOptionDialog(null, "Seleccione el filtro de búsqueda que desear realizar:",
                "Búsqueda Avanzada",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opciones, opciones[0]);

        //Hago un switch para darle el valor a la lista dependiendo del tipo de búsqueda seleccionado
        //así le doy el valor correcto dependiendo del método asignado
        switch (x) {
            case 0:
                miListaProyecto = dProyecto.buscarProyectoId(Integer.parseInt(txtIdProyecto.getText()));
                break;
            case 1:
                miListaProyecto = dProyecto.buscarProyectoNombre(txtNombre.getText());
                break;
            case 2:
                miListaProyecto = dProyecto.buscarProyectoResponsable(cmbResponsable.getSelectedItem().toString());
                break;
            case 3:
                miListaProyecto = dProyecto.buscarProyectoFechaInicio(LocalDate.parse(ftxtFechaInicio.getText()));
                break;
            case 4:
                miListaProyecto = dProyecto.buscarProyectoFechaFinal(LocalDate.parse(ftxtFechaFinal.getText()));
                break;
            case 5:
                miListaProyecto = dProyecto.buscarProyectoFinalizada(finalizado());
                break;
            default:
                miListaProyecto = new ArrayList<>();
                break;
        }

        if (miListaProyecto != null && !miListaProyecto.isEmpty()) {
            String datos[] = new String[6];
            for (Proyecto proyecto : miListaProyecto) {
                datos[0] = String.valueOf(proyecto.getIdProyecto());
                datos[1] = proyecto.getNombreProyecto();
                datos[2] = proyecto.getPersonaResp();
                datos[3] = String.valueOf(proyecto.getFechaInicio());
                datos[4] = String.valueOf(proyecto.getFechaFinal());
                datos[5] = proyecto.isFinalizada() ? "Sí" : "No";
                model.addRow(datos);
            }
        } else {
            this.toBack();
            JOptionPane.showMessageDialog(null, "No se encontraron resultados.", "Búsqueda Avanzada", JOptionPane.INFORMATION_MESSAGE);
        }
        jtProyecto.setModel(model);
    }//GEN-LAST:event_btnBuscarActionPerformed

